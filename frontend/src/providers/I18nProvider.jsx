const I18nContext = createContext(null);

export const I18nProvider = ({ children }) => {
  const [lang, setLang] = useState(() => localStorage.getItem("lgss.lang") || "tr");
  useEffect(() => { localStorage.setItem("lgss.lang", lang); }, [lang]);

  const t = (key, vars = {}) => {
    const dict = translations[lang] || translations.tr;
    let s = dict[key] || key;
    Object.entries(vars).forEach(([k, v]) => { s = s.replace(`{${k}}`, String(v)); });
    return s;
  };

  const value = useMemo(() => ({ lang, setLang, t, languages: Object.keys(translations) }), [lang]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);
